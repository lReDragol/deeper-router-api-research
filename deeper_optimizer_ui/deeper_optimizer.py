class ScanWorker(QThread):
    progress = Signal(str)
    tunnel_done = Signal(dict, list, dict)
    done = Signal(dict)
    failed = Signal(str)

    def __init__(
        self,
        base_url: str,
        cookie_path: Path,
        tunnels: list[dict[str, Any]],
        test_domain: str,
        test_url: str,
        bytes_target: int,
        timeout_seconds: int,
        ip_stabilize_seconds: float,
    ):
        super().__init__()
        self.base_url = base_url
        self.cookie_path = cookie_path
        self.tunnels = tunnels
        self.test_domain = test_domain
        self.test_url = test_url
        self.bytes_target = bytes_target
        self.timeout_seconds = timeout_seconds
        self.ip_stabilize_seconds = ip_stabilize_seconds
        self._stop_event = threading.Event()

    def stop(self) -> None:
        self._stop_event.set()

    def _stopped(self) -> bool:
        return self._stop_event.is_set()

    def run(self):  # noqa: D401
        api = DeeperApiClient(self.base_url, self.cookie_path)
        all_rows: list[dict[str, Any]] = []
        try:
            for idx, t in enumerate(self.tunnels, start=1):
                if self._stopped():
                    break

                tunnel_code = str(t.get("tunnelCode", ""))
                self.progress.emit(f"[{idx}/{len(self.tunnels)}] Scanning {tunnel_code} ...")
                summary, rows = self.scan_tunnel_all_ips(api, t)
                self.tunnel_done.emit(t, rows, summary)
                all_rows.append({"tunnel": t, "summary": summary, "rows": rows})

            self.done.emit({"count": len(all_rows), "results": all_rows, "stopped": self._stopped()})
        except Exception as exc:
            self.failed.emit(f"Scan failed: {exc}\n{traceback.format_exc()}")

    def scan_tunnel_all_ips(self, api: DeeperApiClient, tunnel: dict[str, Any]) -> tuple[dict[str, Any], list[dict[str, Any]]]:
        per_ip: list[dict[str, Any]] = []
        prev_mode = None
        total = max(0, as_int(tunnel.get("activeNum"), 0))
        tunnel_code = str(tunnel.get("tunnelCode", ""))

        if total <= 0:
            return {"sumMbps": 0.0, "ipCount": 0, "loops": 0}, per_ip

        try:
            mode_data = api.get_lb_mode()
            prev_mode = str(mode_data.get("mode") or "")
            if prev_mode and prev_mode != "none":
                api.set_lb_mode("none")

            api.ensure_test_domain_route(self.test_domain, tunnel_code)

            seen: set[str] = set()
            current_ip = str(tunnel.get("activeIp") or "")
            max_loops = max(12, total * 6 + 12)
            guard = 0

            while len(seen) < total and guard < max_loops:
                if self._stopped():
                    break

                guard += 1
                if not current_ip or current_ip in seen:
                    sw = api.switch_node(tunnel_code, current_ip or "0.0.0.0")
                    current_ip = str(sw.get("activeIp") or "")
                    time.sleep(self.ip_stabilize_seconds)
                    continue

                time.sleep(self.ip_stabilize_seconds)
                r = measure_download_robust(self.test_url, self.bytes_target, self.timeout_seconds)
                row = {
                    "ip": current_ip,
                    "mbps": round(as_float(r.get("mbps"), 0.0), 2),
                    "ms": as_int(r.get("ms"), 0),
                    "bytes": as_int(r.get("bytes"), 0),
                    "status": as_int(r.get("status"), 0),
                    "usedRange": bool(r.get("usedRange")),
                }
                per_ip.append(row)
                sw = api.switch_node(tunnel_code, current_ip)
                seen.add(current_ip)
                current_ip = str(sw.get("activeIp") or "")

            sum_mbps = round(sum(as_float(p.get("mbps"), 0.0) for p in per_ip), 2)
            return {"sumMbps": sum_mbps, "ipCount": len(per_ip), "loops": guard}, per_ip
        finally:
            try:
                if prev_mode and prev_mode != "none":
                    api.set_lb_mode(prev_mode)
            except Exception:
                pass


class FailoverWorker(QThread):
    done = Signal(dict)
    failed = Signal(str)

    def __init__(self, base_url: str, cookie_path: Path, db_path: Path, auto_apply: bool = True):
        super().__init__()
        self.base_url = base_url
        self.cookie_path = cookie_path
        self.db_path = db_path
        self.auto_apply = auto_apply

    def run(self):
        api = DeeperApiClient(self.base_url, self.cookie_path)
        store = StatsStorage(self.db_path)
        try:
            mode = api.get_dpn_mode()
            tunnels = api.list_tunnels()
            tmap = {str(t.get("tunnelCode", "")): t for t in tunnels}

            dpn_mode = str(mode.get("dpnMode") or "")
            if dpn_mode == "smart":
                current = str(mode.get("smartTunnel") or mode.get("tunnelCode") or "")
            else:
                current = str(mode.get("fullTunnel") or mode.get("tunnelCode") or "")

            current_tunnel = tmap.get(current)
            healthy = bool(current_tunnel and as_int(current_tunnel.get("activeNum"), 0) > 0)

            if healthy:
                self.done.emit({"status": "ok", "current": current, "reason": "active tunnel healthy", "switched": False})
                return

            ranking = store.get_top_candidates(limit=20)
            candidate = None
            for r in ranking:
                code = str(r.get("tunnelCode", ""))
                if code == current:
                    continue
                t = tmap.get(code)
                if t and as_int(t.get("activeNum"), 0) > 0:
                    candidate = t
                    break

            if candidate is None:
                snapshot = store.get_latest_snapshot("list_tunnels")
                if isinstance(snapshot, list):
                    for row in snapshot:
                        rc = str(row.get("regionCode") or "")
                        cc = str(row.get("countryCode") or "")
                        code = str(row.get("tunnelCode") or "")
                        if not (rc and cc):
                            continue
                        try:
                            api.add_tunnel(rc, cc)
                            time.sleep(0.4)
                        except Exception:
                            continue
                        tunnels = api.list_tunnels()
                        tmap = {str(t.get("tunnelCode", "")): t for t in tunnels}
                        if code in tmap and as_int(tmap[code].get("activeNum"), 0) > 0:
                            candidate = tmap[code]
                            break

            if not candidate:
                self.done.emit(
                    {
                        "status": "no_backup",
                        "current": current,
                        "reason": "no healthy backup tunnel in cache/snapshot",
                        "switched": False,
                    }
                )
                return

            if not self.auto_apply:
                self.done.emit(
                    {
                        "status": "candidate",
                        "current": current,
                        "candidate": candidate.get("tunnelCode"),
                        "reason": "current unhealthy",
                        "switched": False,
                    }
                )
                return

            target_code = str(candidate.get("tunnelCode") or "")
            new_mode = dict(mode)
            if dpn_mode == "smart":
                new_mode["smartTunnel"] = target_code
            else:
                new_mode["dpnMode"] = "full"
                new_mode["fullTunnel"] = target_code
                new_mode["tunnelCode"] = target_code

            api.set_dpn_mode(new_mode)
            current_ip = str(candidate.get("activeIp") or "0.0.0.0")
            try:
                api.switch_node(target_code, current_ip)
            except Exception:
                pass

            self.done.emit(
                {
                    "status": "switched",
                    "current": current,
                    "candidate": target_code,
                    "reason": "current unhealthy",
                    "switched": True,
                }
            )
        except Exception as exc:
            self.failed.emit(f"Failover check failed: {exc}\n{traceback.format_exc()}")


def redact_text(text: str) -> str:
    text = re.sub(r"\b(?:\d{1,3}\.){3}\d{1,3}\b", "<IP_REDACTED>", text)
    text = re.sub(r"Deeper-[A-Za-z0-9\-_|]+", "<DEVICE_ID_REDACTED>", text)
    text = re.sub(r"eyJ[A-Za-z0-9_\-]{10,}\.[A-Za-z0-9_\-]{10,}\.[A-Za-z0-9_\-]{10,}", "<TOKEN_REDACTED>", text)
    return text


def redact_json(value: Any) -> Any:
    if isinstance(value, dict):
        out = {}
        for k, v in value.items():
            if str(k).lower() in {"cookie", "token", "authorization", "password", "deviceid"}:
                out[k] = "<REDACTED>"
            else:
                out[k] = redact_json(v)
        return out
    if isinstance(value, list):
        return [redact_json(v) for v in value]
    if isinstance(value, str):
        return redact_text(value)
    return value


def build_public_api_archive(base_dir: Path) -> Path:
    public_dir = base_dir / "public_docs"
    if public_dir.exists():
        for p in sorted(public_dir.rglob("*"), reverse=True):
            if p.is_file():
                p.unlink()
            elif p.is_dir():
                try:
                    p.rmdir()
                except OSError:
                    pass
    public_dir.mkdir(parents=True, exist_ok=True)

    source_files = [
        "ROUTER_API_FULL.md",
        "router_api_catalog.json",
        "router_api_openapi_like.json",
        "README.md",
    ]

    for name in source_files:
        src = base_dir / name
        if not src.exists():
            continue
        dst_name = name.replace(".md", "_PUBLIC.md").replace(".json", "_PUBLIC.json")
        dst = public_dir / dst_name
        if src.suffix.lower() == ".json":
            try:
                data = json.loads(src.read_text("utf-8"))
                dst.write_text(json.dumps(redact_json(data), ensure_ascii=False, indent=2), encoding="utf-8")
            except Exception:
                dst.write_text(redact_text(src.read_text("utf-8", errors="ignore")), encoding="utf-8")
        else:
            dst.write_text(redact_text(src.read_text("utf-8", errors="ignore")), encoding="utf-8")

    readme = public_dir / "README_PUBLIC.txt"
    readme.write_text(
        "Public API docs archive generated by Deeper Optimizer.\n"
        "Sensitive values (IP/device/token/password-like strings) are redacted.\n"
        "No session cookies or local measurement databases are included.\n",
        encoding="utf-8",
    )

    zip_path = base_dir / "deeper_api_docs_public.zip"
    with zipfile.ZipFile(zip_path, "w", compression=zipfile.ZIP_DEFLATED) as zf:
        for p in sorted(public_dir.rglob("*")):
            if p.is_file():
                zf.write(p, arcname=p.relative_to(public_dir))
    return zip_path


