        for r, t in enumerate(rows):
            code = str(t.get("tunnelCode") or "")
            st = stats.get(code, {})
            if as_int(t.get("activeNum"), 0) > 0:
                alive += 1

            vals = [
                code,
                str(t.get("regionCode") or ""),
                str(t.get("countryCode") or ""),
                str(as_int(t.get("activeNum"), 0)),
                str(t.get("activeIp") or ""),
                f"{as_float(st.get('avg_mbps', 0.0)):.2f}",
                f"{as_float(st.get('max_mbps', 0.0)):.2f}",
                str(as_int(st.get("samples"), 0)),
                str(as_int(st.get("uniq_ips"), 0)),
                str(st.get("last_ts") or ""),
            ]
            for c, v in enumerate(vals):
                item = QTableWidgetItem(v)
                item.setData(Qt.UserRole, t)
                self.table.setItem(r, c, item)

        self.table.resizeColumnsToContents()

        top = self.store.get_top_candidates(limit=1)
