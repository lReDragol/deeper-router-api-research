    def get_whitelist_all(self) -> list[dict[str, Any]]:
        out: list[dict[str, Any]] = []
        page_no = 1
        page_size = 100
        while True:
            data = self.request(
                "GET",
                "/api/smartRoute/getRoutingWhitelist/domain",
                params={"pageNo": page_no, "pageSize": page_size},
            )
            if not isinstance(data, dict):
                break
            rows = data.get("list") if isinstance(data.get("list"), list) else []
            if not rows:
                break
            out.extend(rows)
            if len(rows) < page_size:
                break
            page_no += 1
        return out

    def add_whitelist(self, domain_name: str, tunnel_code: str) -> dict[str, Any]:
        return self.request("POST", "/api/smartRoute/addToWhitelist/domain", json_body={"domainName": domain_name, "tunnelCode": tunnel_code})

